    private void navigationTreeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_navigationTreeMouseClicked
        // TODO add your handling code here:
        DefaultMutableTreeNode selectedPath = (DefaultMutableTreeNode)navigationTree.getLastSelectedPathComponent();
        currentView = selectedPath.toString();
        
        switch(currentView) {
            case "Knjige":
                dataTable.setModel(BookHelper.getTableModel());
                break;            
            case "Bibliotekari":
                dataTable.setModel(LibrarianHelper.getTableModel());
                break;
            case "Studenti":
                dataTable.setModel(StudentHelper.getTableModel());
                break;
            default:
                break;
        }        
    }//GEN-LAST:event_navigationTreeMouseClicked

